    public void muestra() {

        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0);
        for (Trabajador x : at.listado()) {

            Object v[] = {x.getCodigo(), x.getNombre(), x.getSueldoBruto(), x.sueldoNeto()};

            dt.addRow(v);
        }
    }

